package 응용문제;

public class 반복문1 {

	public static void main(String[] args) {
		
		int count = 10;
		while (count > 0) {
			System.out.print(count + "  ");
			count--;
		}
		
		System.out.println();
		
		for (int i = 1; i < 11; i++) {
			System.out.print(i + "  ");
			
		}
		
	}

}

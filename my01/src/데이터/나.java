package 데이터;

public class 나 {

	public static void main(String[] args) {
		// 기본 데이터 연습
		int 나이 = 100;  // 정수
		double 시력 = 1.5;  // 실수
		char 성별 = '여';  // 한 글자
		boolean 저녁 = true;  //논리
		
	// 자동완성  
	// 실행(run) 단축키 :  ctrl + F11
	// syso + ctrl + space bar
		System.out.println("내 나이는 " + 나이 + "세");
		System.out.println("내 시력은 " + 시력);
		System.out.println("내 성별은 " + 성별 + "자");
		System.out.println("나는 저녁을 먹었다 : " + 저녁);
		
	}

}
